Arizachi O`bekistonga kelgandan so`ng 10 kundan kech bo`lmagan muddat ichida Sertifikatni doimiy yashash joyidagi ichki ishlar idoralariga pasport olish uchun topshirilishi lozim. <br>

Sertifikat olish uchun 50 AQSH dollari miqdoridagi konsullik to`lovlari va 5 (MDH davlatlarida)-10 (boshqa davlatlarda) AQSH dollari miqdoridagi ma’muriy xarajatlar undiriladi. Bunda 16 yoshga to`lmagan bolalar konsullik to`lovlaridan ozod etiladi.
