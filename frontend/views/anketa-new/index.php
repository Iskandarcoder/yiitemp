<p>
Arizachiga Sertifikat O`zbekiston Respublikasi vakolatli idoralari tomonidan uning shaxsi tasdiqlangandan so`ng beriladi.<br>

Sertifikat bir oy, istisno holatlarda uch oygacha muddatga beriladi. Sertifikatning muddati uzaytirilmaydi.<br>

Sertifikat har bir kishiga alohida rasmiylashtiriladi. Sertifikatga bolalar yozilmaydi.<br>

Shaxsni tasdiqlovchi hujjat yaroqsiz holga kelgan, muddati o`tgan va viza varaqlari tugagan holda mazkur hujjat Konsullik muassasasiga topshirilishi lozim.<br>

